#!/usr/bin/env node

/**
 * Kanava AI Context Setup
 *
 * Copies AI context files and SKILL.md guides from node_modules
 * into .kanava/ at the consumer's project root, making them
 * visible to AI coding agents that don't scan node_modules.
 *
 * Usage: npx kanava-setup-ai
 */

import { readFileSync, writeFileSync, mkdirSync, cpSync, existsSync, readdirSync } from "node:fs";
import { resolve, join, dirname } from "node:path";

const cwd = process.cwd();
const kanavaDir = join(cwd, ".kanava");

// Find packages in node_modules
const corePkg = resolvePackage("@kanava/editor");
const reactPkg = resolvePackage("@kanava/editor-react");

if (!corePkg) {
  console.error("  [FAIL] @kanava/editor not found in node_modules");
  process.exit(1);
}

console.log("");
console.log("  Kanava AI Context Setup");
console.log("  =======================");
console.log("");

// Create .kanava/ directory
mkdirSync(kanavaDir, { recursive: true });
mkdirSync(join(kanavaDir, "skills"), { recursive: true });

let fileCount = 0;

// Copy AI_CONTEXT.md files
if (corePkg) {
  fileCount += copyIfExists(join(corePkg, "AI_CONTEXT.md"), join(kanavaDir, "AI_CONTEXT_CORE.md"));
  fileCount += copySkills(corePkg, kanavaDir);
}

if (reactPkg) {
  fileCount += copyIfExists(join(reactPkg, "AI_CONTEXT.md"), join(kanavaDir, "AI_CONTEXT_REACT.md"));
  fileCount += copySkills(reactPkg, kanavaDir);
}

// Create a merged index file
const indexContent = buildIndex(corePkg, reactPkg);
writeFileSync(join(kanavaDir, "INDEX.md"), indexContent);
fileCount++;

console.log("");
console.log(`  [OK] Created .kanava/ with ${fileCount} files`);
console.log("  Tip: Add .kanava/ to .gitignore (it's regenerable via npx kanava-setup-ai)");
console.log("");

// ─── Helpers ────────────────────────────────────────────────

function resolvePackage(name) {
  try {
    const pkgJson = join(cwd, "node_modules", name, "package.json");
    if (existsSync(pkgJson)) {
      return dirname(pkgJson);
    }
  } catch {}
  return null;
}

function copyIfExists(src, dest) {
  if (existsSync(src)) {
    writeFileSync(dest, readFileSync(src, "utf-8"));
    console.log(`  COPY ${dest.replace(cwd, ".")}`);
    return 1;
  }
  return 0;
}

function copySkills(pkgPath, outDir) {
  const skillsDir = join(pkgPath, "skills");
  if (!existsSync(skillsDir)) return 0;

  let count = 0;
  for (const skillName of readdirSync(skillsDir)) {
    const skillFile = join(skillsDir, skillName, "SKILL.md");
    if (existsSync(skillFile)) {
      const destDir = join(outDir, "skills", skillName);
      mkdirSync(destDir, { recursive: true });
      writeFileSync(
        join(destDir, "SKILL.md"),
        readFileSync(skillFile, "utf-8")
      );
      console.log(`  COPY .kanava/skills/${skillName}/SKILL.md`);
      count++;
    }
  }
  return count;
}

function buildIndex(corePath, reactPath) {
  const lines = [
    "# Kanava AI Context Index",
    "",
    "This directory was generated by `npx kanava-setup-ai`.",
    "Regenerate at any time by running that command again.",
    "",
    "## Files",
    "",
    "| File | Description |",
    "|------|-------------|",
    "| `AI_CONTEXT_CORE.md` | @kanava/editor — concepts, imports, API, common mistakes |",
    "| `AI_CONTEXT_REACT.md` | @kanava/editor-react — components, hooks, architecture |",
  ];

  if (corePath) {
    const agents = readAgentsField(corePath);
    for (const skill of agents) {
      lines.push(`| \`skills/${skill.name}/SKILL.md\` | ${skill.name} — step-by-step guide |`);
    }
  }
  if (reactPath) {
    const agents = readAgentsField(reactPath);
    for (const skill of agents) {
      lines.push(`| \`skills/${skill.name}/SKILL.md\` | ${skill.name} — step-by-step guide |`);
    }
  }

  lines.push("");
  lines.push("## Reading Order");
  lines.push("");
  lines.push("1. `AI_CONTEXT_CORE.md` — understand concepts and imports");
  lines.push("2. `AI_CONTEXT_REACT.md` — React integration and components");
  lines.push("3. Skills as needed for specific tasks");

  return lines.join("\n") + "\n";
}

function readAgentsField(pkgPath) {
  try {
    const pkg = JSON.parse(readFileSync(join(pkgPath, "package.json"), "utf-8"));
    return pkg.agents?.skills || [];
  } catch {
    return [];
  }
}
